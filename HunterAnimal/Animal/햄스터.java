package Animal;
public class 햄스터 extends 동물 {

	public 햄스터() {
		this.이름 = "햄햄";
		this.set납치확률(90); //햄스터 사냥확률 90
		this.set구조확률(80); //햄스터 구조확률 80
	}

	public 햄스터(String name) {
		// 공통적인 처리부분은 기본생성자에서 처리하고 호출해서 사용하자.
		this();
		this.이름 = name;
	}

	public 햄스터(String name, int 사냥확률, int 구조확률) {
		// 변수에 직접 대입하는 방식도 가능하고, 아래처럼 호출하는 것도 가능
		this(name);
		this.set납치확률(사냥확률);
		this.set구조확률(구조확률);
	}
    public void 굴파기(){
    	System.out.println("햄스터가 굴을 팝니다");
    }

    public void 해바라기씨먹기(){
    	System.out.println("햄스터가 해바라기씨를 먹습니다~ 냠냠");
    }

    public void 놀기(){
    	System.out.println("햄스터가 숲속에서 놉니다~");
    	해바라기씨먹기();
    	굴파기();
    }
    
    public void 재주부리기(){
    	System.out.println("햄햄 해바라기씨 빨리 까기!!");
    	this.power += 1;
    	System.out.println("햄스터의 근력이 1 증가했습니다 현재 : " + this.power);
    }

}
